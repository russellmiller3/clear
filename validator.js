const BALANCE_WATCHLIST = ['balance', 'stock', 'inventory', 'quantity', 'credits', 'remaining', 'available'];

/**
 * Warn when subtracting from balance/stock/inventory fields without a guard.
 */
function validateArithmetic(body, warnings) {
  function check(nodes, hasGuard) {
    for (const n of nodes) {
      if (n.type === NodeType.GUARD) hasGuard = true;
      if (n.type === 'assign' && n.name && n.name.includes('.')) {
        const member = n.name.split('.').pop();
        if (BALANCE_WATCHLIST.includes(member) && n.expression &&
            n.expression.type === 'binary_op' && n.expression.operator === '-' && !hasGuard) {
          warnings.push(
            `Line ${n.line}: subtracting from '${member}' with no guard. Consider adding:\n` +
            `  check ${n.name.replace('.', "'s ")} is at least <amount>, otherwise error 'Insufficient ${member}'`
          );
        }
      }
      if (n.body) check(n.body, hasGuard);
    }
  }
  for (const n of body) {
    if (n.type === NodeType.ENDPOINT && n.body) check(n.body, false);
  }
}

const CAPACITY_FIELDS = ['capacity', 'limit', 'stock'];
const COUNTER_FIELDS = ['tickets_sold', 'sold', 'used', 'registered_count', 'enrolled_count'];

/**
 * Warn when inserting into child of capacity table without a guard.
 */
function validateCapacity(body, warnings) {
  const capacityTables = new Map();
  for (const n of body) {
    if (n.type !== NodeType.DATA_SHAPE) continue;
    const fields = n.fields.map(f => f.name);
    const hasCap = fields.some(f => CAPACITY_FIELDS.some(c => f === c || f.startsWith('max_')));
    const hasCounter = fields.some(f => COUNTER_FIELDS.some(c => f === c || f.endsWith('_count') || f.endsWith('_sold')));
    if (hasCap && hasCounter) capacityTables.set(n.name.toLowerCase(), n);
  }
  if (capacityTables.size === 0) return;

  for (const ep of body) {
    if (ep.type !== NodeType.ENDPOINT || !ep.body) continue;
    const hasGuard = ep.body.some(n => n.type === NodeType.GUARD);
    if (hasGuard) continue;
    for (const n of ep.body) {
      if (n.type !== NodeType.CRUD || n.operation !== 'save' || !n.resultVar) continue;
      const targetName = n.target?.toLowerCase();
      if (!targetName) continue;
      const targetShape = body.find(b => b.type === NodeType.DATA_SHAPE &&
        (b.name.toLowerCase() === targetName || b.name.toLowerCase() === targetName + 's' || b.name.toLowerCase() + 's' === targetName));
      if (!targetShape) continue;
      for (const f of targetShape.fields) {
        if (f.fieldType === 'fk' || f.name.endsWith('_id')) {
          const refBase = f.name.replace(/_id$/, '').toLowerCase();
          for (const [capName] of capacityTables) {
            if (capName.startsWith(refBase) || refBase.startsWith(capName.replace(/s$/, ''))) {
              warnings.push(
                `Line ${ep.line}: inserting into '${targetName}' which references '${capName}'. ` +
                `The ${capName} table has capacity/counter fields but this endpoint has no guard.`
              );
            }
          }
        }
      }
    }
  }
}

/**
 * Warn when frontend sends fields that don't match the backend table schema.
 */
function validateFieldMismatch(body, warnings) {
  // Collect table schemas
  const schemas = new Map();
  for (const n of body) {
    if (n.type === NodeType.DATA_SHAPE) {
      const fields = new Set(n.fields.map(f => f.name));
      schemas.set(n.name.toLowerCase(), { fields, name: n.name });
      const plural = n.name.toLowerCase() + (n.name.toLowerCase().endsWith('s') ? '' : 's');
      schemas.set(plural, { fields, name: n.name });
    }
  }
  // Collect endpoints and their target tables
  const endpointTables = new Map();
  for (const ep of body) {
    if (ep.type !== NodeType.ENDPOINT || !ep.body) continue;
    const crud = ep.body.find(n => n.type === NodeType.CRUD && n.operation === 'save');
    if (crud && crud.target) {
      // Only map endpoint→table when the save target is the receivingVar (direct passthrough).
      // If the endpoint manually constructs a record before saving, the frontend field
      // names don't need to match the table columns.
      const receivingVar = ep.receivingVar || 'data';
      const saveSource = crud.expression?.name || crud.variable || '';
      if (saveSource === receivingVar || !saveSource) {
        const key = `${ep.method} ${ep.path}`;
        const target = crud.target.toLowerCase();
        endpointTables.set(key, target);
        endpointTables.set(key + ':plural', target + (target.endsWith('s') ? '' : 's'));
      }
    }
  }
  // Check frontend API calls
  function checkCalls(nodes) {
    for (const n of nodes) {
      if (n.type === NodeType.API_CALL && n.fields && n.fields.length > 0 && n.url) {
        const method = n.method || 'POST';
        const key = `${method} ${n.url}`;
        const tableName = endpointTables.get(key) || endpointTables.get(key + ':plural');
        if (tableName) {
          const schema = schemas.get(tableName) || schemas.get(tableName + 's');
          if (schema) {
            for (const sent of n.fields) {
              // 'nothing' means POST with no body — not a real field name
              if (sent === 'nothing' || sent === 'null' || sent === 'none') continue;
              if (!schema.fields.has(sent)) {
                let suggestion = '';
                for (const sf of schema.fields) {
                  if (sent.includes(sf) || sf.includes(sent)) suggestion = ` Did you mean '${sf}'?`;
                }
                warnings.push(
                  `Line ${n.line}: frontend sends '${sent}' to ${method} ${n.url}, but the ${schema.name} table has no '${sent}' field.${suggestion}`
                );
              }
            }
          }
        }
      }
      if (n.body) checkCalls(n.body);
    }
  }
  checkCalls(body);
}

/**
 * OWASP TOP 10 SECURITY CHECKS
 *
 * Catches security vulnerabilities at compile time based on the OWASP Top 10:2025.
 * Each check maps to a specific OWASP category.
 */
function validateOWASP(body, errors, warnings) {
  // ── A03: SQL Injection via raw queries with string interpolation ──
  // Raw queries that use string concatenation instead of parameterized queries
  function checkInjection(nodes) {
    for (const n of nodes) {
      if (n.type === NodeType.RAW_QUERY && n.sql) {
        // Check if SQL contains interpolation markers like {variable} or ' + variable
        if (n.sql.includes('{') || n.sql.includes("' +") || n.sql.includes("\" +")) {
          warnings.push(
            `Line ${n.line}: SQL query uses string interpolation which is vulnerable to SQL injection. ` +
            `Use parameterized queries instead: query 'SELECT * FROM users WHERE id = $1' with user_id`
          );
        }
      }
      // Check for raw SQL in assignments too
      if (n.type === NodeType.ASSIGN && n.expression?.type === NodeType.RAW_QUERY) {
        if (n.expression.sql && (n.expression.sql.includes('{') || n.expression.sql.includes("' +"))) {
          warnings.push(
            `Line ${n.line}: SQL query uses string interpolation — this is a SQL injection risk. ` +
            `Use parameterized queries: query 'SELECT * WHERE id = $1' with id_var`
          );
        }
      }
      if (n.body) checkInjection(n.body);
    }
  }
  checkInjection(body);

  // ── A04: Insecure Direct Object References (path traversal in file ops) ──
  // File operations that use user-controlled paths without validation
  function checkPathTraversal(nodes) {
    for (const n of nodes) {
      if (n.type === NodeType.FILE_OP) {
        // If file path is a variable reference (not a literal), warn about path traversal
        if (n.path && n.path.type === NodeType.VARIABLE_REF) {
          warnings.push(
            `Line ${n.line}: File operation uses a variable path ('${n.path.name}'). ` +
            `If this comes from user input, it could allow path traversal attacks (../../etc/passwd). ` +
            `Validate the path or use a fixed directory prefix.`
          );
        }
      }
      if (n.body) checkPathTraversal(n.body);
    }
  }
  checkPathTraversal(body);

  // ── A05: Security Misconfiguration — PATCH endpoints without auth ──
  function checkPatchWithoutAuth(nodes) {
    for (const n of nodes) {
      if (n.type === NodeType.ENDPOINT && n.method === 'PATCH') {
        const hasAuth = n.body?.some(b =>
          b.type === NodeType.REQUIRES_AUTH || b.type === NodeType.REQUIRES_ROLE
        );
        if (!hasAuth) {
          errors.push({
            line: n.line,
            patchable: true,
            fix: ['  requires login'],
            insertAfter: n.line,
            message: `PATCH ${n.path} has no auth guard — anyone can modify data. Add: requires login`
          });
        }
      }
      if (n.body) checkPatchWithoutAuth(n.body);
    }
  }
  checkPatchWithoutAuth(body);

  // ── A07: Cross-Site Request Forgery — POST endpoints that modify data without auth ──
  // POST endpoints that save/update/delete without auth are CSRF-vulnerable
  function checkCSRF(nodes) {
    for (const n of nodes) {
      if (n.type !== NodeType.ENDPOINT) continue;
      if (n.method !== 'POST') continue;
      const hasAuth = n.body?.some(b =>
        b.type === NodeType.REQUIRES_AUTH || b.type === NodeType.REQUIRES_ROLE
      );
      const hasCrud = n.body?.some(b => n.type === NodeType.CRUD);
      const modifiesData = n.body?.some(b =>
        b.type === NodeType.CRUD && (b.operation === 'save' || b.operation === 'remove')
      );
      if (modifiesData && !hasAuth) {
        const path = n.path?.toLowerCase() || '';
        // Skip signup/register — those are intentionally unauthenticated
        if (path.includes('signup') || path.includes('register') || path.includes('seed')) continue;
        warnings.push(
          `Line ${n.line}: POST ${n.path} modifies data without auth — CSRF vulnerable. ` +
          `A malicious site could trick a user's browser into submitting data. ` +
          `Fix: add 'requires login' inside the endpoint.`
        );
      }
    }
  }
  checkCSRF(body);

  // ── A09: Security Logging — no logging on apps with auth ──
  const hasAuth = body.some(n =>
    n.type === NodeType.ENDPOINT && n.body &&
    n.body.some(b => b.type === NodeType.REQUIRES_AUTH || b.type === NodeType.REQUIRES_ROLE)
  );
  const hasLogging = body.some(n => n.type === NodeType.LOG_REQUESTS);
  if (hasAuth && !hasLogging) {
    warnings.push(
      `Your app requires login but doesn't log requests. Without logging, you can't detect ` +
      `unauthorized access attempts or debug auth issues. Add: log every request`
    );
  }
}

/**
 * Validate agent tool references — verify each tool name matches a defined function.
 * Also validate guardrails: check that tool functions don't violate must-not restrictions.
 */
function validateAgentTools(body, errors) {
  // Collect all function definitions
  const functionDefs = {};
  for (const node of body) {
    if (node.type === NodeType.FUNCTION_DEF) functionDefs[node.name] = node;
  }

  // Helper: scan a function body for CRUD operations
  function findCrudOps(fnBody) {
    const ops = [];
    function scan(nodes) {
      for (const n of nodes) {
        if (n.type === NodeType.CRUD) {
          ops.push({ operation: n.operation, target: n.target });
        }
        if (n.body) scan(n.body);
        if (n.thenBranch) scan(n.thenBranch);
        if (n.otherwiseBranch) scan(n.otherwiseBranch);
        // Check expression-level CRUD (assignments with CRUD expressions)
        if (n.type === NodeType.ASSIGN && n.expression && n.expression.type === NodeType.CRUD) {
          ops.push({ operation: n.expression.operation, target: n.expression.target });
        }
      }
    }
    scan(fnBody);
    return ops;
  }

  // Check each agent
  for (const node of body) {
    if (node.type !== NodeType.AGENT) continue;

    // Validate tool references exist
    if (node.tools && node.tools.length > 0) {
      for (const tool of node.tools) {
        if (tool.type === 'ref' && !functionDefs[tool.name]) {
          errors.push({
            line: node.line,
            message: `agent '${node.name}' uses tool '${tool.name}' but no function '${tool.name}' is defined. Add: define function ${tool.name}(...):`
          });
        }
      }
    }

    // Validate guardrails: check tool functions against must-not restrictions
    if (node.restrictions && node.restrictions.length > 0 && node.tools && node.tools.length > 0) {
      const compileRestrictions = node.restrictions.filter(r => r.category === 'delete' || r.category === 'modify' || r.category === 'access');
      for (const tool of node.tools) {
        if (tool.type !== 'ref') continue;
        const fnDef = functionDefs[tool.name];
        if (!fnDef) continue;
        const crudOps = findCrudOps(fnDef.body);
        for (const restriction of compileRestrictions) {
          for (const op of crudOps) {
            if (restriction.category === 'delete' && (op.operation === 'remove' || op.operation === 'delete')) {
              errors.push({
                line: node.line,
                message: `agent '${node.name}' uses '${tool.name}' which deletes from ${op.target}, but the agent has 'must not: ${restriction.text}'. Remove the restriction or change the tool.`
              });
            }
            if (restriction.category === 'access') {
              // Extract table name from restriction: "access Users table" or "access Users"
              const tableMatch = restriction.text.match(/access\s+(\w+)/);
              const restrictedTable = tableMatch ? tableMatch[1] : '';
              // Handle singular/plural: "User" matches "Users" and vice versa
              const singularTarget = op.target.replace(/s$/, '');
              const singularRestricted = restrictedTable.replace(/s$/, '');
              if (restrictedTable && (op.target === restrictedTable || singularTarget === singularRestricted)) {
                errors.push({
                  line: node.line,
                  message: `agent '${node.name}' uses '${tool.name}' which accesses ${op.target}, but the agent has 'must not: ${restriction.text}'. Remove the restriction or change the tool.`
                });
              }
            }
            if (restriction.category === 'modify' && (op.operation === 'save' || op.operation === 'update' || op.operation === 'insert')) {
              const tableMatch = restriction.text.match(/modify\s+(\w+)/);
              const restrictedTable = tableMatch ? tableMatch[1] : '';
              if (restrictedTable && op.target === restrictedTable) {
                errors.push({
                  line: node.line,
                  message: `agent '${node.name}' uses '${tool.name}' which modifies ${op.target}, but the agent has 'must not: ${restriction.text}'. Remove the restriction or change the tool.`
                });
              }
            }
          }
        }
      }
    }
  }
}

// =============================================================================
// VALIDATE CALL TARGETS: agent, pipeline, workflow names must exist
// =============================================================================
function validateCallTargets(body, errors) {
  const agents = new Set();
  const pipelines = new Set();
  const workflows = new Set();
  for (const node of body) {
    if (node.type === NodeType.AGENT) agents.add(node.name.toLowerCase().replace(/\s+/g, '_'));
    if (node.type === NodeType.PIPELINE) pipelines.add(node.name.toLowerCase().replace(/\s+/g, '_'));
    if (node.type === NodeType.WORKFLOW) workflows.add(node.name.toLowerCase().replace(/\s+/g, '_'));
  }

  function checkExpr(expr, line) {
    if (!expr) return;
    if (expr.type === NodeType.RUN_AGENT && expr.agentName) {
      const normalized = expr.agentName.toLowerCase().replace(/\s+/g, '_');
      if (!agents.has(normalized)) {
        const all = [...agents];
        const hint = all.length > 0 ? ` Defined agents: ${all.join(', ')}` : '';
        errors.push({ line: expr.line || line, message: `agent '${expr.agentName}' is not defined.${hint}` });
      }
    }
    if (expr.type === NodeType.RUN_PIPELINE && expr.pipelineName) {
      const normalized = expr.pipelineName.toLowerCase().replace(/\s+/g, '_');
      if (!pipelines.has(normalized)) {
        errors.push({ line: expr.line || line, message: `pipeline '${expr.pipelineName}' is not defined. Add: pipeline '${expr.pipelineName}' with var:` });
      }
    }
    if (expr.type === NodeType.RUN_WORKFLOW && expr.workflowName) {
      const normalized = expr.workflowName.toLowerCase().replace(/\s+/g, '_');
      if (!workflows.has(normalized)) {
        errors.push({ line: expr.line || line, message: `workflow '${expr.workflowName}' is not defined. Add: workflow '${expr.workflowName}' with state:` });
      }
    }
    if (expr.left) checkExpr(expr.left, line);
    if (expr.right) checkExpr(expr.right, line);
    if (expr.argument) checkExpr(expr.argument, line);
  }

  function checkNodes(nodes) {
    for (const node of nodes) {
      if (node.type === NodeType.ASSIGN && node.expression) checkExpr(node.expression, node.line);
      if (node.body) checkNodes(node.body);
      if (node.thenBranch && Array.isArray(node.thenBranch)) checkNodes(node.thenBranch);
      if (node.otherwiseBranch && Array.isArray(node.otherwiseBranch)) checkNodes(node.otherwiseBranch);
      if (node.tryBody) checkNodes(node.tryBody);
      if (node.handleBody) checkNodes(node.handleBody);
    }
  }
  checkNodes(body);
}

// =============================================================================
// VALIDATE MEMBER ACCESS TYPES: warn about field access on known primitives
// =============================================================================
function validateMemberAccessTypes(body, warnings) {
  const varTypes = new Map();

  function inferType(expr) {
    if (!expr) return 'unknown';
    if (expr.type === 'literal_number' || expr.type === 'number') return 'number';
    if (expr.type === 'literal_string' || expr.type === 'string') return 'string';
    if (expr.type === 'literal_boolean' || expr.type === 'boolean') return 'boolean';
    if (expr.type === 'literal_record') return 'object';
    if (expr.type === 'literal_list') return 'list';
    if (expr.type === NodeType.ASK_AI && expr.schema && expr.schema.length > 0) return 'object';
    if (expr.type === NodeType.RUN_AGENT || expr.type === NodeType.RUN_WORKFLOW) return 'object';
    if (expr.type === 'binary_op') {
      const op = expr.operator;
      if (['+', '-', '*', '/', '%'].includes(op)) return 'number';
    }
    if (expr.type === 'variable_ref' && varTypes.has(expr.name)) return varTypes.get(expr.name);
    return 'unknown';
  }

  function checkNodes(nodes) {
    for (const node of nodes) {
      if (node.type === NodeType.ASSIGN && node.expression) {
        const t = inferType(node.expression);
        if (t !== 'unknown') varTypes.set(node.name, t);
      }
      if (node.type === NodeType.ASSIGN && node.expression?.type === 'member_access') {
        const obj = node.expression.object;
        const objName = obj?.name || obj?.value;
        if (objName && varTypes.has(objName)) {
          const t = varTypes.get(objName);
          if (t === 'number' || t === 'boolean') {
            warnings.push({
              line: node.line,
              message: `'${objName}' is a ${t}, not an object — accessing '${node.expression.property}' on it will fail at runtime.`
            });
          }
        }
      }
      if (node.body) checkNodes(node.body);
      if (node.thenBranch && Array.isArray(node.thenBranch)) checkNodes(node.thenBranch);
      if (node.otherwiseBranch && Array.isArray(node.otherwiseBranch)) checkNodes(node.otherwiseBranch);
    }
  }
  checkNodes(body);
}


// =============================================================================
// VALIDATE TYPE ANNOTATIONS: Warn on literal type mismatches at call sites.
// Only checks literal args (string, number, boolean) against typed params.
// =============================================================================
function validateTypedCallArgs(body, warnings) {
  // Build map of typed function signatures
  const typedFns = {};
  function collectDefs(nodes) {
    if (!Array.isArray(nodes)) return;
    for (const node of nodes) {
      if (node.type === NodeType.FUNCTION_DEF && node.params?.some(p => p.type)) {
        typedFns[node.name] = node.params;
      }
      if (Array.isArray(node.body)) collectDefs(node.body);
      if (Array.isArray(node.thenBranch)) collectDefs(node.thenBranch);
      if (Array.isArray(node.otherwiseBranch)) collectDefs(node.otherwiseBranch);
      if (node.handlers) node.handlers.forEach(h => collectDefs(h.body));
    }
  }
  collectDefs(body);

  const literalType = (expr) => {
    if (!expr) return null;
    if (expr.type === 'literal_string') return 'text';
    if (expr.type === 'literal_number') return 'number';
    if (expr.type === 'literal_boolean') return 'boolean';
    return null;
  };

  const compatible = (expected, actual) => {
    if (!expected || !actual) return true;
    if (expected === actual) return true;
    return false;
  };

  function checkExprs(nodes) {
    if (!Array.isArray(nodes)) return;
    for (const node of nodes) {
      checkNodeExprs(node);
      if (Array.isArray(node.body)) checkExprs(node.body);
      if (Array.isArray(node.thenBranch)) checkExprs(node.thenBranch);
      if (Array.isArray(node.otherwiseBranch)) checkExprs(node.otherwiseBranch);
      if (node.handlers) node.handlers.forEach(h => checkExprs(h.body));
    }
  }

  function checkCall(expr, line) {
    if (!expr || expr.type !== 'call' || !expr.name) return;
    const params = typedFns[expr.name];
    if (!params) return;
    for (let i = 0; i < Math.min(params.length, (expr.args || []).length); i++) {
      const expectedType = params[i].type;
      if (!expectedType) continue;
      const argType = literalType(expr.args[i]);
      if (argType && !compatible(expectedType, argType)) {
        warnings.push({
          line,
          message: `Type mismatch: '${params[i].name}' expects ${expectedType} but got ${argType} in call to '${expr.name}()'`
        });
      }
    }
    for (const arg of expr.args || []) checkExprForCalls(arg, line);
  }

  function checkExprForCalls(expr, line) {
    if (!expr) return;
    if (expr.type === 'call') checkCall(expr, line);
    if (expr.left) checkExprForCalls(expr.left, line);
    if (expr.right) checkExprForCalls(expr.right, line);
    if (expr.object) checkExprForCalls(expr.object, line);
    if (expr.args) expr.args.forEach(a => checkExprForCalls(a, line));
  }

  function checkNodeExprs(node) {
    if (!node) return;
    const line = node.line;
    if (node.expression) checkExprForCalls(node.expression, line);
    if (node.condition) checkExprForCalls(node.condition, line);
    if (node.value) checkExprForCalls(node.value, line);
  }

  checkExprs(body);
}

/**
 * INFERRED TYPE SYSTEM (Phase P1):
 * Walk assignments to build a type environment, then error on arithmetic on text.
 * Only fires when type is known from a literal assignment — never guesses.
 *
 * Catches:
 *   price = 'ten dollars'
 *   total = price * 1.08     ← error: price is text, used in arithmetic
 *
 * Uses typed param annotations (Phase 97) as authoritative type source.
 */
function validateInferredTypes(body, errors) {
  // Infer the type of a literal or known expression
  function inferLiteralType(expr) {
    if (!expr) return 'unknown';
    if (expr.type === NodeType.LITERAL_STRING) return 'text';
    if (expr.type === NodeType.LITERAL_NUMBER) return 'number';
    if (expr.type === NodeType.LITERAL_BOOLEAN) return 'boolean';
    if (expr.type === NodeType.LITERAL_LIST) return 'list';
    if (expr.type === NodeType.STRING_INTERPOLATION) return 'text';
    // Arithmetic always produces a number (if both sides are numbers)
    if (expr.type === NodeType.BINARY_OP) {
      if (['-', '*', '/', '%', '**'].includes(expr.operator)) return 'number';
      if (['+'].includes(expr.operator)) {
        const l = inferLiteralType(expr.left);
        const r = inferLiteralType(expr.right);
        if (l === 'number' && r === 'number') return 'number';
        if (l === 'text' || r === 'text') return 'text';
      }
    }
    return 'unknown';
  }

  // Build type environment by scanning all assignments in a node list
  function buildTypeEnv(nodes, env) {
    if (!Array.isArray(nodes)) return;
    for (const node of nodes) {
      if (node.type === NodeType.ASSIGN) {
        const t = inferLiteralType(node.expression);
        if (t !== 'unknown') env.set(node.name, { type: t, line: node.line });
      }
      // Typed function params are authoritative
      if (node.type === NodeType.FUNCTION_DEF && Array.isArray(node.params)) {
        for (const p of node.params) {
          if (p.type) {
            const mapped = p.type === 'text' ? 'text' : p.type === 'number' ? 'number' : p.type === 'boolean' ? 'boolean' : 'unknown';
            if (mapped !== 'unknown') env.set(p.name, { type: mapped, line: node.line, isParam: true });
          }
        }
        buildTypeEnv(node.body, env);
      }
      if (node.type === NodeType.ENDPOINT) buildTypeEnv(node.body, env);
      if (node.type === NodeType.FOR_EACH) buildTypeEnv(node.body, env);
      if (Array.isArray(node.thenBranch)) buildTypeEnv(node.thenBranch, env);
      if (Array.isArray(node.otherwiseBranch)) buildTypeEnv(node.otherwiseBranch, env);
    }
  }

  // Resolve the type of an expression given the current env
  function resolveType(expr, env) {
    if (!expr) return 'unknown';
    if (expr.type === NodeType.LITERAL_STRING) return 'text';
    if (expr.type === NodeType.LITERAL_NUMBER) return 'number';
    if (expr.type === NodeType.LITERAL_BOOLEAN) return 'boolean';
    if (expr.type === NodeType.STRING_INTERPOLATION) return 'text';
    if (expr.type === NodeType.VARIABLE_REF) return env.get(expr.name)?.type || 'unknown';
    if (expr.type === NodeType.BINARY_OP) {
      if (['-', '*', '/', '%', '**'].includes(expr.operator)) return 'number';
    }
    return 'unknown';
  }

  // Check expressions for type mismatches
  function checkExpr(expr, env, line) {
    if (!expr) return;
    if (expr.type === NodeType.BINARY_OP) {
      const op = expr.operator;
      // Arithmetic operators expect numbers
      if (['-', '*', '/', '%', '**'].includes(op)) {
        const lt = resolveType(expr.left, env);
        const rt = resolveType(expr.right, env);
        if (lt === 'text') {
          const varName = expr.left?.name || expr.left?.value || '?';
          errors.push({ line: expr.line || line, message: `'${varName}' is text, not a number — can't use it in ${op} arithmetic. Assign a number instead.` });
        } else if (rt === 'text') {
          const varName = expr.right?.name || expr.right?.value || '?';
          errors.push({ line: expr.line || line, message: `'${varName}' is text, not a number — can't use it in ${op} arithmetic. Assign a number instead.` });
        }
      }
      checkExpr(expr.left, env, line);
      checkExpr(expr.right, env, line);
    }
    if (expr.args) expr.args.forEach(a => checkExpr(a, env, line));
    if (expr.object) checkExpr(expr.object, env, line);
  }

  function walkNodes(nodes, env) {
    if (!Array.isArray(nodes)) return;
    for (const node of nodes) {
      const line = node.line;
      // Check expressions in assignments, conditions, and values
      if (node.type === NodeType.ASSIGN) {
        checkExpr(node.expression, env, line);
        // Update env with inferred type after checking
        const t = inferLiteralType(node.expression);
        if (t !== 'unknown') env.set(node.name, { type: t, line });
      } else if (node.expression) {
        checkExpr(node.expression, env, line);
      }
      if (node.condition) checkExpr(node.condition, env, line);
      if (node.value) checkExpr(node.value, env, line);
      // Recurse
      if (Array.isArray(node.body)) walkNodes(node.body, new Map(env));
      if (Array.isArray(node.thenBranch)) walkNodes(node.thenBranch, new Map(env));
      if (Array.isArray(node.otherwiseBranch)) walkNodes(node.otherwiseBranch, new Map(env));
      if (node.handlers) node.handlers.forEach(h => walkNodes(h.body, new Map(env)));
    }
  }

  // Build global type env, then walk checking for mismatches
  const globalEnv = new Map();
  buildTypeEnv(body, globalEnv);
  walkNodes(body, globalEnv);
}

// CHAIN DEPTH: warn when possessive chains go deeper than 4 levels.
// "user's profile's avatar's url's host" is 4 deep and already pushing it.
// Deep chains are a symptom of missing intermediate variables — exactly what
// Clear's one-operation-per-line rule is designed to prevent.
function validateChainDepth(body, warnings) {
  const MAX_DEPTH = 4;

  function chainDepth(expr) {
    if (!expr || expr.type !== NodeType.MEMBER_ACCESS) return 0;
    return 1 + chainDepth(expr.object);
  }

  function checkExpr(expr, line) {
    if (!expr) return;
    const depth = chainDepth(expr);
    if (depth >= MAX_DEPTH) {
      warnings.push({
        line: expr.line || line,
        message: `Possessive chain is ${depth} levels deep — break it into named steps. Example:\n  profile = user's profile\n  avatar = profile's avatar\n  url = avatar's url`,
      });
    }
    // Still recurse to catch chains nested inside other expressions
    if (expr.object) checkExpr(expr.object, line);
    if (expr.left) checkExpr(expr.left, line);
    if (expr.right) checkExpr(expr.right, line);
    if (expr.args) expr.args.forEach(a => checkExpr(a, line));
    if (expr.condition) checkExpr(expr.condition, line);
    if (expr.expression) checkExpr(expr.expression, line);
  }

  function walk(nodes) {
    if (!Array.isArray(nodes)) return;
    for (const node of nodes) {
      const line = node.line;
      if (node.expression) checkExpr(node.expression, line);
      if (node.condition) checkExpr(node.condition, line);
      if (node.value) checkExpr(node.value, line);
      if (Array.isArray(node.body)) walk(node.body);
      if (Array.isArray(node.thenBranch)) walk(node.thenBranch);
      if (Array.isArray(node.otherwiseBranch)) walk(node.otherwiseBranch);
      if (node.handlers) node.handlers.forEach(h => walk(h.body));
    }
  }

  walk(body);
}

// EXPRESSION COMPLEXITY: warn when a single expression uses 3+ binary operators.
// That's a sign the line is doing too much — Clear's design says name intermediates.
// Example: a = b plus c times d minus e   → warn, suggest breaking up
function validateExprComplexity(body, warnings) {
  const MAX_OPS = 2;

  function countOps(expr) {
    if (!expr) return 0;
    if (expr.type !== NodeType.BINARY_OP) return 0;
    return 1 + countOps(expr.left) + countOps(expr.right);
  }

  function checkExpr(expr, line) {
    if (!expr) return;
    if (expr.type === NodeType.BINARY_OP) {
      const ops = countOps(expr);
      if (ops > MAX_OPS) {
        warnings.push({
          line: expr.line || line,
          message: `This expression has ${ops} operations on one line. Break it into named steps:\n  step1 = ...\n  step2 = step1 ...`,
        });
        return; // Don't recurse — one warning per top-level expression
      }
    }
    if (expr.left) checkExpr(expr.left, line);
    if (expr.right) checkExpr(expr.right, line);
    if (expr.args) expr.args.forEach(a => checkExpr(a, line));
    if (expr.object) checkExpr(expr.object, line);
    if (expr.expression) checkExpr(expr.expression, line);
  }

  function walk(nodes) {
    if (!Array.isArray(nodes)) return;
    for (const node of nodes) {
      const line = node.line;
      if (node.expression) checkExpr(node.expression, line);
      // Codex chunk #1: skip the condition-complexity check on DB lookups —
      // filter expressions like `where status is 'pending' and demo_key is X`
      // tripped the noise threshold even though they're declarative not logic.
      const isDatabaseLookup = node.type === NodeType.CRUD && node.operation === 'lookup';
      if (node.condition && !isDatabaseLookup) checkExpr(node.condition, line);
      if (Array.isArray(node.body)) walk(node.body);
      if (Array.isArray(node.thenBranch)) walk(node.thenBranch);
      if (Array.isArray(node.otherwiseBranch)) walk(node.otherwiseBranch);
      if (node.handlers) node.handlers.forEach(h => walk(h.body));
    }
  }

  walk(body);
}

/**
 * CLASSIFY VALIDATION: classify needs at least 2 categories.
 */
function validateClassify(body, errors) {
  function walk(nodes) {
    for (const node of nodes) {
      if (node.type === NodeType.ASSIGN && node.expression?.type === NodeType.CLASSIFY) {
        if (!node.expression.categories || node.expression.categories.length < 2) {
          errors.push({
            line: node.line,
            message: `classify needs at least 2 categories. Example: intent = classify message as 'order', 'return'`
          });
        }
      }
      if (Array.isArray(node.body)) walk(node.body);
      if (Array.isArray(node.thenBranch)) walk(node.thenBranch);
      if (Array.isArray(node.otherwiseBranch)) walk(node.otherwiseBranch);
      if (node.handlers) node.handlers.forEach(h => walk(h.body));
    }
  }

  walk(body);
}
