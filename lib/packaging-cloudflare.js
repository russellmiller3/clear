// lib/packaging-cloudflare.js
// Cloudflare Workers for Platforms bundle emitter.
//
// When `compileProgram(src, { target: 'cloudflare' })` fires, the compiler
// hands us the AST + the already-emitted Node serverJS/html/tests, and we
// translate that into the file layout Workers for Platforms expects:
//
//   src/index.js        ESM default-export fetch handler — routes by URL.pathname
//   wrangler.toml       name, main, compat_date + flags, (later: bindings)
//   migrations/*.sql    (Phase 2) D1 schema migrations
//   static/*            (later) static assets
//
// This phase (Phase 1) only produces src/index.js + wrangler.toml. D1,
// workflows, crons, secrets come in later phases.
//
// Design notes:
// - All compatibility_date / compatibility_flags live as single-source
//   constants at the top of this file. One place to upgrade.
// - `wranglerTomlTemplate()` is a pure function — trivially testable in
//   isolation without spinning the full compiler.
// - The emitted Worker MUST be ESM-only. No `require()`. Workers-for-Platforms
//   rejects CommonJS scripts at upload time, and `esbuild --bundle` can't
//   mix `require` into ESM output cleanly either.
// - `data-clear-line="N"` attributes inside any embedded HTML string MUST
//   survive — click-to-edit (future plan) depends on them. We just ship
//   `result.html` through as a string literal.

import { mkdirSync, writeFileSync, readFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';
import { NodeType } from '../parser.js';
import { UTILITY_FUNCTIONS_WORKERS } from '../compiler.js';

const __cfDirname = dirname(fileURLToPath(import.meta.url));

// ─── Workers-safe auth source (loaded once, cached) ─────────────────────
// The ESM source at runtime/auth-webcrypto.mjs is the SINGLE SOURCE OF
// TRUTH for password hashing. When a Workers bundle needs auth, we read
// that file's text and inline the function bodies directly. `export`
// keywords are stripped because the Workers bundle has only one top-level
// `export default { fetch }` — every helper is a file-local function.
let _authWebcryptoSource = null;
function _loadAuthWebcryptoSource() {
	if (_authWebcryptoSource) return _authWebcryptoSource;
	const p = resolve(__cfDirname, '..', 'runtime', 'auth-webcrypto.mjs');
	const raw = readFileSync(p, 'utf8');
	// Strip `export ` prefix from function / const declarations — a plain
	// inlined bundle can't use export statements outside its single
	// top-level export default. JSDoc comments + internals stay.
	const stripped = raw.replace(/^export\s+(async\s+function|function|const)\s/gm, '$1 ');
	_authWebcryptoSource = stripped;
	return stripped;
}

// ─── compat pins (SINGLE SOURCE OF TRUTH) ─────────────────────────────────
// Bump these when Cloudflare releases a newer compat that we've tested.
export const CF_COMPAT_DATE = '2025-04-01';
export const CF_COMPAT_FLAGS = ['nodejs_compat_v2'];

/**
 * Produce the text of a wrangler.toml file for a Worker.
 * Pure function — unit-testable without compiling a Clear program.
 *
 * @param {object} opts
 * @param {string} opts.name    Worker script name (usually the app slug).
 * @param {string} [opts.main]  Entry file (default: 'src/index.js').
 * @returns {string} TOML body
 */
export function wranglerTomlTemplate(opts = {}) {
	const name = (opts.name || 'clear-app').replace(/[^a-z0-9-]/gi, '-').toLowerCase();
	const main = opts.main || 'src/index.js';
	const flagsTOML = '[' + CF_COMPAT_FLAGS.map((f) => `"${f}"`).join(', ') + ']';
	return [
		`# wrangler.toml — generated by Clear for Cloudflare Workers for Platforms`,
		`# Pinned compat date + flags are single-sourced in lib/packaging-cloudflare.js`,
		`name = "${name}"`,
		`main = "${main}"`,
		`compatibility_date = "${CF_COMPAT_DATE}"`,
		`compatibility_flags = ${flagsTOML}`,
		``,
	].join('\n');
}

/**
 * Emit src/index.js — the ESM fetch handler that routes requests.
 *
 * Phase 1 shape:
 * - ESM default-export object with async fetch(request, env, ctx)
 * - Routes by `new URL(request.url).pathname` + method
 * - Each ENDPOINT produces one route branch that responds with a stub Response
 * - Embeds `result.html` (when present) behind GET / so click-to-edit
 *   data-clear-line attributes survive into the Worker's text response
 *
 * No `require()`, no `fs`, no `child_process` — Workers runtime has none of these.
 *
 * @param {Array} astBody    AST body array
 * @param {object} compiled  Existing compile() result (has .html if web target)
 * @returns {string} source text of src/index.js
 */
export function emitWorkerIndexJs(astBody, compiled = {}) {
	const endpoints = collectEndpoints(astBody);
	const html = typeof compiled.html === 'string' ? compiled.html : '';

	const lines = [];
	lines.push('// Generated by Clear for Cloudflare Workers for Platforms');
	lines.push('// DO NOT EDIT — regenerate by recompiling the .clear source');
	lines.push('');
	// Embed HTML as a JSON-stringified literal so data-clear-line attrs and
	// any backticks / template markers in the HTML survive byte-for-byte.
	lines.push(`const __CLEAR_HTML__ = ${JSON.stringify(html)};`);
	lines.push('');

	// Inline Workers-safe utility functions for AI / agent features. We scan
	// the AST for ASK_AI + AGENT + tool-use nodes and select the matching
	// helpers from UTILITY_FUNCTIONS_WORKERS. This keeps the bundle small
	// (no helpers when the app has no AI features) and, critically, ensures
	// the Workers-safe variants are the ONLY AI plumbing a deployed Worker
	// ever sees. Zero `require()`, zero `fs.`, zero `child_process` — those
	// only live inside UTILITY_FUNCTIONS (the Node set) and never reach
	// the Workers bundle.
	const needed = _selectWorkersUtilities(astBody);
	if (needed.length > 0) {
		lines.push('// --- Workers-safe runtime helpers (inlined) ---');
		for (const code of needed) {
			lines.push(code);
		}
		lines.push('');
	}

	// Inline the Web Crypto auth helpers when the app uses `allow signup
	// and login`. Reads from runtime/auth-webcrypto.mjs so a single source
	// of truth (same PBKDF2 iteration count, same hash format) lives in
	// one file and the compiler just ships its contents.
	const usesAuth = astBody.some((n) => n && n.type === NodeType.AUTH_SCAFFOLD);
	if (usesAuth) {
		lines.push('// --- Web Crypto auth (PBKDF2 via crypto.subtle) — inlined from runtime/auth-webcrypto.mjs ---');
		lines.push(_loadAuthWebcryptoSource());
		lines.push('');
	}

	lines.push('export default {');
	lines.push('  async fetch(request, env, ctx) {');
	lines.push('    const url = new URL(request.url);');
	lines.push('    const pathname = url.pathname;');
	lines.push('    const method = request.method;');
	lines.push('');

	// Emit one route block per endpoint. Phase 1 is a stub: just echo back
	// the endpoint path + method so the bundle is smoke-testable under
	// `wrangler dev`. Real endpoint body codegen lands in later phases.
	for (const ep of endpoints) {
		const methodUpper = (ep.method || 'GET').toUpperCase();
		const safePath = JSON.stringify(ep.path);
		lines.push(`    if (method === ${JSON.stringify(methodUpper)} && pathname === ${safePath}) {`);
		lines.push(`      return new Response(JSON.stringify({ ok: true, endpoint: ${safePath} }), {`);
		lines.push(`        headers: { 'Content-Type': 'application/json' }`);
		lines.push(`      });`);
		lines.push(`    }`);
	}

	// GET / → serve the compiled HTML (with data-clear-line attrs intact)
	lines.push(`    if (method === 'GET' && (pathname === '/' || pathname === '/index.html')) {`);
	lines.push(`      return new Response(__CLEAR_HTML__, {`);
	lines.push(`        headers: { 'Content-Type': 'text/html; charset=utf-8' }`);
	lines.push(`      });`);
	lines.push(`    }`);
	lines.push('');
	lines.push(`    return new Response('Not found', { status: 404 });`);
	lines.push('  }');
	lines.push('};');
	lines.push('');

	return lines.join('\n');
}

/**
 * Build the complete workerBundle: every file that gets written to disk.
 * Caller decides where the files land (`packageBundle` in Phase 1.6).
 *
 * @param {Array} astBody
 * @param {object} compiled  compile() result
 * @param {object} [opts]
 * @param {string} [opts.appName]  Script name for wrangler.toml
 * @returns {object} { 'src/index.js': string, 'wrangler.toml': string, ... }
 */
export function buildWorkerBundle(astBody, compiled = {}, opts = {}) {
	const bundle = {};
	bundle['src/index.js'] = emitWorkerIndexJs(astBody, compiled);
	bundle['wrangler.toml'] = wranglerTomlTemplate({ name: opts.appName || 'clear-app' });
	return bundle;
}

/**
 * Write the Workers-for-Platforms bundle to disk.
 *
 * If the compiler already populated `result.workerBundle` (fast path — the
 * caller already compiled with target='cloudflare'), just walk and write.
 * Otherwise rebuild from the AST as a safety net so callers can pass the
 * default-target result and still get Workers output.
 *
 * @param {object} result   compile() result
 * @param {string} outDir   directory to write into
 * @param {object} [opts]
 * @param {string} [opts.appName]  Worker script name
 * @returns {object} { ok, outDir, target, files, appName }
 */
export function packageCloudflareBundle(result, outDir, opts = {}) {
	const appName = opts.appName || 'clear-app';
	const bundle = result.workerBundle && Object.keys(result.workerBundle).length
		? rehydrateBundleAppName(result.workerBundle, appName)
		: buildWorkerBundle(result.ast?.body || [], result, { appName });

	mkdirSync(outDir, { recursive: true });
	const files = [];
	for (const [relPath, contents] of Object.entries(bundle)) {
		const abs = resolve(outDir, relPath);
		mkdirSync(dirname(abs), { recursive: true });
		writeFileSync(abs, contents, 'utf8');
		// Normalize to forward slashes so the caller sees the same keys on Windows + Unix.
		files.push(relPath.replace(/\\/g, '/'));
	}

	return {
		ok: true,
		outDir,
		target: 'cloudflare',
		files,
		appName,
	};
}

// When the compiler pre-populated result.workerBundle, it used a default
// app name ('clear-app'). If the caller now passes opts.appName, rewrite
// wrangler.toml so the script uploads under the right name. Preserves the
// rest of the bundle byte-for-byte (including src/index.js with data-clear-line).
function rehydrateBundleAppName(bundle, appName) {
	const out = { ...bundle };
	if (typeof out['wrangler.toml'] === 'string') {
		out['wrangler.toml'] = wranglerTomlTemplate({ name: appName });
	}
	return out;
}

// ─── internals ────────────────────────────────────────────────────────────

function collectEndpoints(body) {
	const out = [];
	const walk = (nodes) => {
		if (!Array.isArray(nodes)) return;
		for (const n of nodes) {
			if (!n) continue;
			if (n.type === NodeType.ENDPOINT) {
				out.push({ method: n.method, path: n.path });
			}
			if (n.body) walk(n.body);
			if (n.pages) walk(n.pages);
			if (n.thenBranch) walk(n.thenBranch);
			if (n.otherwiseBranch) walk(n.otherwiseBranch);
		}
	};
	walk(body);
	return out;
}

// Walk the AST and figure out which Workers-safe utility functions the Worker
// bundle needs. This is the Worker-target counterpart to the Node-side
// `_getUsedUtilities(compiledCode)` — but we scan the AST instead of compiled
// JS text because the Worker bundle emit is AST-driven (endpoints are stubs
// in the current phase; Phase 4+ will emit full endpoint bodies).
//
// Selection rules:
//   - ASK_AI node present       → _askAI_workers (+ _askAI_core)
//   - AGENT node with tools     → _askAIWithTools_workers
//   - AGENT that streams        → _askAIStream_workers
export function _selectWorkersUtilities(astBody) {
	const flags = _scanAIFlags(astBody);
	if (!flags.hasAskAI && !flags.hasTools && !flags.hasStream) return [];

	const needed = new Set();
	if (flags.hasAskAI) needed.add('_askAI_workers');
	if (flags.hasTools) needed.add('_askAIWithTools_workers');
	if (flags.hasStream) needed.add('_askAIStream_workers');

	// Expand dependencies using the declared .deps on each utility entry.
	const byName = new Map(UTILITY_FUNCTIONS_WORKERS.map((u) => [u.name, u]));
	const queue = [...needed];
	while (queue.length) {
		const name = queue.shift();
		const u = byName.get(name);
		if (!u) continue;
		for (const dep of u.deps || []) {
			if (!needed.has(dep)) {
				needed.add(dep);
				queue.push(dep);
			}
		}
	}

	// Return in the order they're declared in UTILITY_FUNCTIONS_WORKERS (which
	// is authored in dependency order — core comes first).
	return UTILITY_FUNCTIONS_WORKERS.filter((u) => needed.has(u.name)).map((u) => u.code);
}

function _scanAIFlags(body) {
	const out = { hasAskAI: false, hasTools: false, hasStream: false };
	const walk = (nodes) => {
		if (!Array.isArray(nodes)) return;
		for (const n of nodes) {
			if (!n) continue;
			if (n.type === NodeType.ASK_AI) out.hasAskAI = true;
			// Also catch `set x to ask claude ...` where the ASK_AI lives inside
			// an ASSIGN node's expression.
			if (n.type === NodeType.ASSIGN && n.expression && n.expression.type === NodeType.ASK_AI) {
				out.hasAskAI = true;
			}
			if (n.type === NodeType.AGENT) {
				out.hasAskAI = true; // agent bodies reach _askAI internally
				if (Array.isArray(n.tools) && n.tools.length > 0) out.hasTools = true;
				// Streaming agents — conservative: any agent that hasn't explicitly
				// disabled streaming counts. The Node compiler has a more nuanced
				// check (schema/tool/schedule disable); for the Workers bundle we
				// just inline the helper — the emitter decides when to use it.
				if (n.streamResponse !== false && (!n.tools || n.tools.length === 0)) {
					out.hasStream = true;
				}
			}
			if (n.body) walk(n.body);
			if (n.pages) walk(n.pages);
			if (n.thenBranch) walk(n.thenBranch);
			if (n.otherwiseBranch) walk(n.otherwiseBranch);
			if (n.expression && n.expression.body) walk(n.expression.body);
		}
	};
	walk(body);
	return out;
}
